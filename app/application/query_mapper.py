"""기분·날씨·형용사 → 카카오 검색 키워드 매핑 (코드 규칙).

'무엇을 검색할지'는 LLM이 아니라 규칙으로 결정한다.
작은 모델에게 매번 결정시키면 불안정하므로 여기서 고정한다.

형용사("조용한", "활기찬" 등)는 '특정 장소의 속성'(LLM이 모름)이 아니라
'카테고리 선호'로 번역해서 처리한다. (예: 조용 → 북카페/도서관/전시회)
"""

_RAINY = ("비", "눈", "소나기", "장마")

# 야외 카테고리 — 비/눈일 때 최종 결과에서 제거 (날씨 모순 방지)
_OUTDOOR = {"공원", "산책로"}

# 형용사/분위기/기분 → 카테고리 선호. 기분 필드 + 자유 텍스트를 함께 스캔한다.
_VIBE_RULES: list[tuple[tuple[str, ...], list[str]]] = [
    # 조용·차분 계열
    (("조용", "차분", "한적", "힐링", "여유", "혼자", "고요"),
     ["북카페", "도서관", "서점", "전시회", "공원"]),
    # 활기·신남 계열
    (("활기", "신나", "신남", "활발", "에너지", "들뜸"),
     ["맛집", "전시회", "헬스장"]),
    # 감성·분위기 계열
    (("감성", "분위기", "예쁜", "데이트", "뷰"),
     ["카페", "전시회", "공원"]),
    # 운동·활동 계열
    (("운동", "땀", "활동적", "움직"),
     ["헬스장", "공원", "산책로"]),
    # 무기력·저에너지 계열 (부담 적은 곳)
    (("무기력", "우울", "지침", "피곤", "처짐", "쉬고", "쉬"),
     ["카페", "산책로", "공원"]),
]


def pick_search_queries(weather: str, mood: str, text: str = "") -> list[str]:
    """날씨 + (기분·자유텍스트의 형용사) → 검색할 카테고리 목록."""
    rainy = any(w in weather for w in _RAINY)
    blob = f"{mood} {text}"  # 기분 필드 + 사용자 자유 텍스트를 함께 스캔
    queries: set[str] = set()

    # ① 날씨 규칙
    if rainy:
        queries |= {"카페", "전시회", "서점", "영화관"}  # 실내 위주
    else:
        queries |= {"공원", "산책로", "카페"}  # 야외 가능

    # ② 형용사/기분 → 카테고리 선호
    for keywords, cats in _VIBE_RULES:
        if any(k in blob for k in keywords):
            queries |= set(cats)

    # ③ 비/눈이면 야외 카테고리 제거 (형용사가 공원을 넣었어도 날씨 우선)
    if rainy:
        queries -= _OUTDOOR

    return list(queries)
