"""좁히기 선택지(칩) 생성 — 코드 규칙(결정론적), LLM 아님.

막연한 추천 요청일 때 fe가 버튼으로 보여줄 선택지를 만든다.
매번 같은 상황이면 같은 칩이 나와야 하므로(일관성·속도) 규칙으로 고정한다.
칩 텍스트는 클릭 시 그대로 사용자 메시지로 전송되므로,
분류기가 잘 알아듣는 표현(장소종류/분위기)으로 구성한다.
"""

_RAINY = ("비", "눈", "소나기", "장마")
_NIGHT = ("밤", "저녁", "새벽")

# 기본 칩 (낮·맑음 기준)
_BASE = ["맛집", "조용한 카페", "놀 거리", "야외/산책", "문화/전시", "휴식/힐링"]


# 큰 갈래 답변 → 2단계(세부) 질문 분기. LLM이 되묻기를 건너뛰어도 코드가 보장한다.
# 키 = 1단계 버튼/유사 표현, 값 = (질문, 세부 선택지)
_BRANCH: dict[str, tuple[str, list[str]]] = {
    "먹으러 가기": ("어떤 음식이 좋으세요?", ["한식", "중식", "일식", "양식", "분식", "고기"]),
    "맛집": ("어떤 음식이 좋으세요?", ["한식", "중식", "일식", "양식", "분식", "고기"]),
    "놀거리": ("어떤 놀이가 끌리세요?", ["PC방", "볼링장", "노래방", "보드게임카페", "방탈출카페"]),
    "놀 거리": ("어떤 놀이가 끌리세요?", ["PC방", "볼링장", "노래방", "보드게임카페", "방탈출카페"]),
    "문화생활": ("어떤 문화생활이 좋으세요?", ["전시회", "영화관", "박물관", "서점"]),
    "문화/전시": ("어떤 문화생활이 좋으세요?", ["전시회", "영화관", "박물관", "서점"]),
    "휴식": ("어떻게 쉬고 싶으세요?", ["조용한 카페", "공원 산책", "스파/찜질방", "북카페"]),
    "휴식/힐링": ("어떻게 쉬고 싶으세요?", ["조용한 카페", "공원 산책", "스파/찜질방", "북카페"]),
}


def branch_question(text: str) -> tuple[str, list[str]] | None:
    """사용자 답이 '큰 갈래'면 세부 질문·선택지를 반환, 아니면 None."""
    return _BRANCH.get(text.strip())


def pick_options(weather: str = "", time_of_day: str = "") -> list[str]:
    """날씨·시간대에 맞는 좁히기 선택지 목록 (5~6개)."""
    options = list(_BASE)

    # 비/눈 → 야외 계열 제거, 실내 대안 보강
    if any(w in weather for w in _RAINY):
        options.remove("야외/산책")
        options.append("영화")

    # 밤/저녁/새벽 → 야외 산책 대신 술집·야식 계열
    if any(t in time_of_day for t in _NIGHT):
        if "야외/산책" in options:
            options.remove("야외/산책")
        options.append("술집/야식")

    return options[:6]
